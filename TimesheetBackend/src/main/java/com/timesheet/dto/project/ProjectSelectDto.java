package com.timesheet.dto.project;

public class ProjectSelectDto {
    private Integer id;
    private String code;
    private String name;

    public ProjectSelectDto() {
    }

    public ProjectSelectDto(Integer id, String code, String name) {
        this.id = id;
        this.code = code;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
